    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");

            Connection kon = DriverManager.getConnection("jdbc:mysql://localhost/kasir_parfum", "root", "");

            String sql = "INSERT INTO transaksi (nama_barang, harga, jumlah_beli) VALUES (?,?,?)";
            PreparedStatement ps = kon.prepareStatement(sql);

            ps.setString(1, cb_barang.getSelectedItem().toString());
            ps.setString(2, txt_harga.getText());
            ps.setString(3, txt_stok.getText());

            ps.executeUpdate();
           
            int total = Integer.parseInt(txt_harga.getText()) * Integer.parseInt(txt_stok.getText());
            txt_total_harga.setText(String.valueOf(total));

            int bayar = Integer.parseInt(txt_bayar.getText());
            txt_kembalian.setText(String.valueOf(bayar - total));

            String sql_stok = "UPDATE barang SET stok = stok - ? WHERE nama_barang = ?";
            PreparedStatement ps_stok = kon.prepareStatement(sql_stok);
            ps_stok.setString(1, txt_stok.getText());
            ps_stok.setString(2, cb_barang.getSelectedItem().toString());
            ps_stok.executeUpdate();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(new Object[]{
                cb_barang.getSelectedItem(),
                txt_harga.getText(),
                txt_stok.getText()
            });

            JOptionPane.showMessageDialog(null, "BERHASIL SIMPAN KE DATABASE!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Cek lagi, ada kendala: " + e.getMessage());
        }

    }//GEN-LAST:event_SimpanActionPerformed


