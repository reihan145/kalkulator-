    private void cb_barangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_barangActionPerformed
        String NamaBarang = (String) cb_barang.getSelectedItem();

        // Kodingan agar harga muncul otomatis
        if (NamaBarang.equals("parfum kecil")) {
            txt_harga.setText("30000");
        } else if (NamaBarang.equals("parfum sedang")) {
            txt_harga.setText("40000");
        } else if (NamaBarang.equals("besar")) {
            txt_harga.setText("50000");
        }
    }//GEN-LAST:event_cb_barangActionPerformed

