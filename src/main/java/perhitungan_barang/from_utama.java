    private void hitung_jumlah_barangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitung_jumlah_barangActionPerformed
        int harga = Integer.parseInt(txt_harga.getText());
        int beli = Integer.parseInt(txt_stok.getText());
        int total = harga * beli;
        txt_total_harga.setText(java.text.NumberFormat.getIntegerInstance(java.util.Locale.GERMANY).format(total));
    }//GEN-LAST:event_hitung_jumlah_barangActionPerformed

    private void bayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bayarActionPerformed
        int JumlahBayar = Integer.parseInt(txt_bayar.getText().replace(".", "").replace(",", ""));
        int JumlahHarga = Integer.parseInt(txt_total_harga.getText().replace(".", "").replace(".", ""));
        if (JumlahBayar >= JumlahHarga) {
            int kembalian = JumlahBayar - JumlahHarga;
            txt_kembalian.setText(java.text.NumberFormat.getIntegerInstance(java.util.Locale.GERMANY).format(kembalian));
        } else {
            txt_kembalian.setText("Uang Anda Kurang");
        }
    }//GEN-LAST:event_bayarActionPerformed

